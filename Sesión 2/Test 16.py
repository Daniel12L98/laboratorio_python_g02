"""Listas en Python"""

"""Listas sort(): Ordenar los elementos de una lista"""

var_1 = ["SQLServer", "RDS", "MySQL", "Postgresql"]
var_2 = [50, 75, 1560, 400, 3, 4000]

var_1.sort()
var_2.sort()

print("Los valores de mi lista ordenada var_1 es: {}".format(var_1))
print("Los valores de mi lista ordenada var_2 es: {}".format(var_2))
