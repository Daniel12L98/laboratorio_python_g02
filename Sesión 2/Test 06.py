"""Operadores aritméticos en Python"""

"""Operación división e identificación de tipo de dato"""

division_1 = 100/20
division_2 = 1096.50/10
division_3 = 4000.40/2

print("El valor de mi variable division_1 es: {}, "
      "El valor de mi variable division_2 es: {} y "
      "El valor de mi variable division_3 es: {}"
      "".format(division_1, division_2, division_3))

print("El tipo de dato de division_1 es: {}".format(type(division_1)))
print("El tipo de dato de division_2 es: {}".format(type(division_2)))
print("El tipo de dato de division_3 es: {}".format(type(division_3)))

